USE Telecom
GO

CREATE TABLE Abonament(
						ID_Abonament INT PRIMARY KEY,
						Denumire VARCHAR(30),
						PretLunar MONEY
						)
CREATE TABLE Abonat(
			ID_Abonat INT PRIMARY KEY,
			Nume VARCHAR(40),
			Adresa VARCHAR(30),
			IDNP NVARCHAR(13) UNIQUE,
			TipClient VARCHAR(10)
			)

CREATE TABLE Cont(
					ID_Cont INT PRIMARY KEY,
					Numar NVARCHAR(10),
					ID_Abonament INT FOREIGN KEY REFERENCES Abonament(ID_Abonament),
					ID_Abonat INT FOREIGN KEY REFERENCES Abonat(ID_Abonat),
					DataContractare DATE,
					Balansa MONEY,
					Locatie  VARCHAR(10)
				)
CREATE TABLE Optiuni(
					ID_Optiune INT PRIMARY KEY,
					Denumire VARCHAR(20),
					Pret MONEY,
					Durata TINYINT
					)
CREATE TABLE Optiuni_Cont(
							ID_OC  INT PRIMARY KEY,
							ID_Optiune INT FOREIGN KEY REFERENCES Optiuni(ID_Optiune),
							ID_Cont INT FOREIGN KEY REFERENCES Cont(ID_Cont),
							DataActivare DATE
						)
CREATE TABLE Apeluri(
					ID_Apel INT PRIMARY KEY,
					ID_Abonat1 INT FOREIGN KEY REFERENCES Abonat(ID_Abonat),
					ID_Abonat2 INT FOREIGN KEY REFERENCES Abonat(ID_Abonat),
					Durata INT,
					DataApel DATE
					)
CREATE TABLE USERS(
					username varchar(20)  PRIMARY KEY,
					password varchar(20)
					)
